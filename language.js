function settext(key) {
    if (localStorage.lang == 'en') {
        return en[key];
    } else {
        return th[key] 
    }
}

function togglelangen() {
    if (localStorage.lang == 'en')
        return 'now language:' + localStorage.lang;
    else if (localStorage.lang == 'th')
        localStorage.setItem('lang', 'en')

    renderlang();
    changelangcontact();
    return 'now language:' + localStorage.lang;
 }

 function togglelangth() {
    if (localStorage.lang == 'th')
        return 'now language:' + localStorage.lang;
    else if (localStorage.lang == 'en')
        localStorage.setItem('lang', 'th')

    renderlang();
    changelangcontact();
    return 'now language:' + localStorage.lang;
 }

 function checklang() {
    if (localStorage.lang == 'en') {
        document.getElementById("lang_en").style.color = '#00d9ff';
        document.getElementById("lang_th").style.color = '#fff';
    } else {
        document.getElementById("lang_th").style.color = '#00d9ff';
        document.getElementById("lang_en").style.color = '#fff';
    }
 }


